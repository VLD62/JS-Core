function dayOfTheWeek(day) {

    if (day.toLowerCase() == "monday")      return console.log(1);
    if (day.toLowerCase() == "tuesday")     return console.log(2);
    if (day.toLowerCase() == "wednesday")   return console.log(3);
    if (day.toLowerCase() == "thursday")    return console.log(4);
    if (day.toLowerCase() == "friday")      return console.log(5);
    if (day.toLowerCase() == "saturday")    return console.log(6);
    if (day.toLowerCase() == "sunday")      return console.log(7);
    return console.log("error");

}

dayOfTheWeek("sdasfafsfs");